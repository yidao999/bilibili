@ActivityScope
public class UserInfoDetailsPresenter extends BasePresenter<UserInfoDetailsContract.Model, UserInfoDetailsContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    UserDetailsInfo userDetailsInfos;

    @Inject
    public UserInfoDetailsPresenter(UserInfoDetailsContract.Model model, UserInfoDetailsContract.View rootView) {
        super(model, rootView);
    }

    public void getData(int mid) {
        mModel.getData(mid)
                .subscribeOn(Schedulers.io())
                .retryWhen(new RetryWithDelay(3, 3))
                .doOnSubscribe(new Consumer<Disposable>() {
                    @Override
                    public void accept(Disposable disposable) throws Exception {
                        mRootView.showLoading();
                    }
                }).subscribeOn(AndroidSchedulers.mainThread())
                .observeOn(AndroidSchedulers.mainThread())
                .doFinally(new Action() {
                    @Override
                    public void run() throws Exception {
                        mRootView.hideLoading();
                    }
                }).compose(RxLifecycleUtils.bindToLifecycle(mRootView))
                .subscribe(new ErrorHandleSubscriber<UserDetailsInfo>(mErrorHandler) {
                    @Override
                    public void onNext(UserDetailsInfo userDetailsInfo) {
                        mRootView.finishTask(userDetailsInfo);
                    }
                });
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }
}
